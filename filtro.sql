DROP TABLE EMPLEADO
CREATE TABLE EMPLEADO(
COD_EMPLEADO INT NOT NULL,
NOMBRE_COMPLETO VARCHAR(100),
FECHA_NAC DATE,
FECHA_INGRESO DATETIME,
SUELDO NUMERIC(10,2),
ES_NUEVO BIT
)

------MUESTRA SOLO LOS RESULTADOS EN DONDE EL CODIGO EMPLEADO SEA IGUAL A 1
SELECT *
FROM EMPLEADO 
WHERE COD_EMPLEADO = 1

