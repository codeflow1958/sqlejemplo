FROM EMPLEADO


--COMANDOS QUE SE PUEDEN UTILIZAR DENTRO DE UN SELECT
---TOP
-----PERMITE LIMITAR LOS RESULTADOS OBTENIDOS DE UNA CONSULTA SEGUN EL NUMERO QUE SE COLOQUE DENTRO DE LOS PARENTESIS
SELECT TOP(5) *
FROM EMPLEADO

---DISTINCT
------PERMITE MOSTRAR LOS RESULTADOS UNICOS DE UNA CONSULTA, SI AL MENOS 1 COLUMNA NO ES IGUAL, ESTOS NO SE AGRUPAN
