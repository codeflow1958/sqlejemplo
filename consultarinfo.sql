DROP TABLE EMPLEADO
CREATE TABLE EMPLEADO(
COD_EMPLEADO INT NOT NULL,
NOMBRE_COMPLETO VARCHAR(100),
FECHA_NAC DATE,
FECHA_INGRESO DATETIME,
SUELDO NUMERIC(10,2),
ES_NUEVO BIT
)
--MOSTRAR TODA LA INFORMACION DE UNA TABLA
