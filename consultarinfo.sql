--COMANDO AS
----PERMITE CAMBIAR EL NOMBRE DE UNA COLUMNA, SOLO CUANDO SE MUESTRA EL RESULTADO, NO EN LA BASE DE DATOS
SELECT COD_EMPLEADO AS CODIGO, 
